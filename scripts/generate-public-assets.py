def render_changelog_markdown(release):
    lines = [f"## Changes In {release['version']} ({release['date']})", ""]
    for category in release["categories"]:
        if not category["items"]:
            continue
        lines.extend([f"### {category['name']}", ""])
        lines.extend(f"- {item}" for item in category["items"])
        lines.append("")
    return "\n".join(lines).rstrip()


def render_portal_description(info, copy, latest_release):
    lines = [f"# {info['title']}", ""]
    for paragraph in copy["portal_description"]["paragraphs"]:
        lines.extend([paragraph, ""])
    lines.extend(["Highlights:", ""])
    lines.extend(f"- {item}" for item in copy["highlights"])
    lines.extend(["", render_changelog_markdown(latest_release), ""])
    lines.extend(["Source:", copy["source_url"], ""])
    lines.extend(["Homepage:", copy["homepage_url"], ""])
    return "\n".join(lines)


def render_release_notes(info, copy, latest_release):
    lines = [f"# {info['title']} {info['version']}", "", copy["release_notes"]["intro"], ""]
    lines.extend(["## Highlights", ""])
    lines.extend(f"- {item}" for item in copy["highlights"])
    lines.extend(["", render_changelog_markdown(latest_release), ""])
    lines.extend(["## Validation", ""])
    lines.extend(f"- {item}" for item in copy["release_notes"]["validation"])
    lines.extend(["", "## Links", ""])
    lines.extend(
        [
            f"- Playtest guide: {copy['source_url']}/blob/main/docs/PLAYTEST.md",
            f"- Mod Portal: {copy['mod_portal_url']}",
            f"- Homepage: {copy['homepage_url']}",
            "",
        ]
    )
    return "\n".join(lines)


def render_portal_metadata(info, copy):
    fields = {
        "MOD_PORTAL_TITLE": info["title"],
        "MOD_PORTAL_SUMMARY": copy["portal"]["summary"],
        "MOD_PORTAL_CATEGORY": copy["portal"]["category"],
        "MOD_PORTAL_TAGS": copy["portal"]["tags"],
        "MOD_PORTAL_HOMEPAGE": copy["homepage_url"],
        "MOD_PORTAL_SOURCE_URL": copy["source_url"],
    }
    return "".join(f"{key}={shlex.quote(value)}\n" for key, value in fields.items())


def write_text(path, content):
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def repo_path(value):
    path = pathlib.Path(value)
    if path.is_absolute():
        return path
    return ROOT / path


def check_file(path, expected):
    actual = path.read_text(encoding="utf-8") if path.exists() else None
    if actual == expected:
        return True
    try:
        display_path = path.relative_to(ROOT)
    except ValueError:
        display_path = path
    print(f"{display_path} is stale; run scripts/generate-public-assets.py.", file=sys.stderr)
    return False


def main():
    parser = argparse.ArgumentParser(description="Generate public website, release, and Mod Portal assets.")
    parser.add_argument("--check", action="store_true", help="Check that committed generated files are current.")
    parser.add_argument("--site", default=str(SITE_PATH), help="Path to write docs/index.html output.")
    parser.add_argument("--portal-description", help="Path to write generated Mod Portal description Markdown.")
    parser.add_argument("--portal-metadata", help="Path to write generated shell metadata for Mod Portal scripts.")
    parser.add_argument("--release-notes", help="Path to write generated GitHub release notes Markdown.")
    args = parser.parse_args()

    info = load_json(INFO_PATH)
    copy = load_json(COPY_PATH)
    latest_release = parse_changelog(CHANGELOG_PATH)[0]
    validate_sources(info, latest_release)

    site_content = render_site(info, copy, latest_release)

    if args.check:
        if not check_file(repo_path(args.site), site_content):
            return 1
        print("Generated public assets are current.")
        return 0

    write_text(repo_path(args.site), site_content)

    if args.portal_description:
        write_text(repo_path(args.portal_description), render_portal_description(info, copy, latest_release))

    if args.portal_metadata:
        write_text(repo_path(args.portal_metadata), render_portal_metadata(info, copy))

    if args.release_notes:
        write_text(repo_path(args.release_notes), render_release_notes(info, copy, latest_release))

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
