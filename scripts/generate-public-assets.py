def load_json(path):
    with path.open("r", encoding="utf-8") as handle:
        return json.load(handle)


def parse_changelog(path):
    releases = []
    current = None
    current_category = None

    for raw_line in path.read_text(encoding="utf-8").splitlines():
        line = raw_line.rstrip()
        stripped = line.strip()

        if not stripped or set(stripped) == {"-"}:
            continue

        if stripped.startswith("Version:"):
            if current is not None:
                releases.append(current)
            current = {
                "version": stripped.split(":", 1)[1].strip(),
                "date": "",
                "categories": [],
            }
            current_category = None
            continue

        if current is None:
            continue

        if stripped.startswith("Date:"):
            current["date"] = stripped.split(":", 1)[1].strip()
            continue

        if line.startswith("  ") and stripped.endswith(":") and not stripped.startswith("-"):
            current_category = {"name": stripped[:-1], "items": []}
            current["categories"].append(current_category)
            continue

        if line.startswith("    - ") and current_category is not None:
            current_category["items"].append(line[6:])

    if current is not None:
        releases.append(current)

    if not releases:
        raise ValueError(f"No releases found in {path}")
    return releases


def validate_sources(info, latest_release):
    if info["version"] != latest_release["version"]:
        raise ValueError(
            "info.json version "
            f"{info['version']} does not match latest changelog version {latest_release['version']}"
        )


def e(value):
    return html.escape(str(value), quote=True)


def inline_html(value):
    parts = str(value).split("`")
    if len(parts) == 1:
        return e(value)

    output = []
    for index, part in enumerate(parts):
        if index % 2 == 0:
            output.append(e(part))
        else:
            output.append(f"<code>{e(part)}</code>")
    return "".join(output)


def lines_to_html_list(items, indent):
    lines = [f"{indent}<ul>"]
    for item in items:
        lines.append(f"{indent}  <li>{inline_html(item)}</li>")
    lines.append(f"{indent}</ul>")
    return lines


def render_changelog_html(release, indent):
    lines = [f"{indent}<div class=\"release-notes\">"]
    for category in release["categories"]:
        if not category["items"]:
            continue
        lines.append(f"{indent}  <h3>{e(category['name'])}</h3>")
        lines.extend(lines_to_html_list(category["items"], f"{indent}  "))
    lines.append(f"{indent}</div>")
    return lines


